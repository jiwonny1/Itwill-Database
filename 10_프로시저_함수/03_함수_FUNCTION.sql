--BOOKID로 책제목 확인 함수 (파라미터 값: BOOKID, RETURN값: BOOKNAME)
CREATE OR REPLACE FUNCTION GET_BOOKNAME (
    IN_ID IN NUMBER
) RETURN VARCHAR2 --리턴데이터 타입
AS
    V_BOOKNAME BOOK.BOOKNAME%TYPE;
BEGIN
    SELECT BOOKNAME 
    INTO V_BOOKNAME
    FROM BOOK
    WHERE BOOKID = IN_ID;
    
    RETURN V_BOOKNAME;
END;
--------------------
--함수의 사용(SELECT, WHERE 절에서 사용)
--동작여부 확인
SELECT GET_BOOKNAME(5) FROM DUAL;
--SELECT 절에서 사용
SELECT BOOKID, BOOKNAME, GET_BOOKNAME(BOOKID)
FROM BOOK;
----------
SELECT O.*, GET_BOOKNAME(BOOKID)
FROM ORDERS O;
---------
-- WHERE 절에서 함수 사용
SELECT O.*
     , GET_BOOKNAME(O.BOOKID) AS BOOKNAME
FROM ORDERS O
WHERE GET_BOOKNAME(O.BOOKID) LIKE '%야구%'
;
