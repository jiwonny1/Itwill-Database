--(실습) 고객ID 값을 받아서 고객명을 돌려주는 함수 작성(CUSTOMER 테이블 사용)
-- 함수명 : GET_CUSTNAME
-- GET_CUSTNAME 함수 사용해서 고객명 검색 여부 확인해보기
-------------------
-- ORDERS 테이블 데이터 조회
---- GET_BOOKNAME, GET_CUSTNAME 함수사용 주문(판매)정보와 책제목, 고객명 조회
---------------------

CREATE OR REPLACE FUNCTION GET_CUSTNAME(
    IN_ID IN NUMBER
) RETURN VARCHAR2
AS V_NAME CUSTOMER.NAME%TYPE;
BEGIN 
    SELECT NAME   
    INTO V_NAME
    FROM CUSTOMER
    WHERE CUSTID = IN_ID;
    
    RETURN V_NAME;
END;

SELECT GET_CUSTNAME(2) FROM DUAL;

SELECT O.*, GET_CUSTNAME(O.CUSTID)
FROM ORDERS O
;

SELECT O.*, GET_BOOKNAME(O.BOOKID), GET_CUSTNAME(O.CUSTID)
FROM ORDERS O;
-------------------
SELECT O.*
     , (SELECT BOOKNAME FROM BOOK WHERE BOOKID = O.BOOKID) AS BOOK_NAME
     , (SELECT NAME FROM CUSTOMER WHERE CUSTID = O.CUSTID) AS CUST_NAME
FROM ORDERS O
;
-----------
SELECT O.*
     , B.BOOKNAME
     , C.NAME
FROM ORDERS O, BOOK B, CUSTOMER C
WHERE O.BOOKID = B.BOOKID
  AND O.CUSTID = C.CUSTID
;
